
public class AbsoluteValue {

	public static void main(String[] args) {
		        int number = -25;
		        int absolute;

		        if (number < 0) {
		            absolute = -number;
		        } else {
		            absolute = number;
		        }

		        System.out.println("The absolute value of " + number + " is " + absolute);
		    }
	
}
